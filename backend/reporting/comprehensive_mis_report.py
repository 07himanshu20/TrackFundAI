    # ── Burn & Runway (from PortfolioKPI or CompanyFinancials) ────────
    burn_runway = []
    BURN_SLUGS = {
        'gross_burn': ['gross-burn', 'burn-rate', 'monthly-burn', 'gross-burn-rate'],
        'net_burn': ['net-burn', 'net-burn-rate'],
        'cash_balance': ['cash', 'cash-and-equivalents', 'cash-balance', 'cash-in-hand'],
        'runway_months': ['runway-months', 'runway', 'cash-runway'],
    }
    slug_to_burn = {}
    for bk, slugs in BURN_SLUGS.items():
        for s in slugs:
            slug_to_burn[s] = bk

    burn_kpis = PortfolioKPI.objects.filter(
        investment__scheme__fund=fund
    ).select_related('kpi_definition', 'investment__portfolio_company').order_by(
        'investment__portfolio_company__name', '-period'
    )

    co_burn = defaultdict(lambda: {'gross_burn': None, 'net_burn': None, 'cash_balance': None, 'runway_months': None, 'period': None})
    for kpi in burn_kpis:
        slug = kpi.kpi_definition.slug.lower()
        burn_key = None
        for pattern, bk in slug_to_burn.items():
            if pattern in slug:
                burn_key = bk
                break
        if not burn_key:
            continue
        co_name = kpi.investment.portfolio_company.name if kpi.investment.portfolio_company else kpi.investment.company_name
        if co_burn[co_name][burn_key] is None:
            co_burn[co_name][burn_key] = float(kpi.value) if kpi.value else None
            if co_burn[co_name]['period'] is None:
                co_burn[co_name]['period'] = str(kpi.period)

    for co_name, vals in sorted(co_burn.items()):
        if any(v is not None for k, v in vals.items() if k != 'period'):
            burn_runway.append({
                'company': co_name,
                'gross_burn': vals.get('gross_burn'),
                'net_burn': vals.get('net_burn'),
                'cash_balance': vals.get('cash_balance'),
                'runway_months': int(vals['runway_months']) if vals.get('runway_months') else None,
                'period': vals.get('period', '—'),
            })

    # Fallback: derive fund-level burn from ConsolidatedMIS if no KPI burn data
    if not burn_runway:
        cons_latest = ConsolidatedMIS.objects.filter(
            fund=fund, organization=org
        ).order_by('-period_year', '-period_month').first()

        if cons_latest:
            cons_period = ConsolidatedMIS.objects.filter(
                fund=fund, organization=org,
                period_year=cons_latest.period_year,
                period_month=cons_latest.period_month
            )
            total_opex = None
            ebitda_val = None
            revenue_val = None
            for c in cons_period:
                if c.line_item == 'total_opex' and c.total_actual_inr:
                    total_opex = float(c.total_actual_inr)
                elif c.line_item == 'ebitda' and c.total_actual_inr:
                    ebitda_val = float(c.total_actual_inr)
                elif c.line_item in ('revenue', 'total_revenue') and c.total_actual_inr:
                    revenue_val = float(c.total_actual_inr)

            # Gross burn = total_opex (monthly), Net burn = opex - revenue (if negative, company is profitable)
            gross_burn = total_opex
            net_burn = (total_opex - revenue_val) if total_opex and revenue_val else total_opex

            # Get cash from NAVRecord
            latest_nav = NAVRecord.objects.filter(scheme__fund=fund).order_by('-nav_date').first()
            cash_bal = float(latest_nav.cash_and_equivalents or 0) if latest_nav else None

            runway = None
            if net_burn and net_burn > 0 and cash_bal and cash_bal > 0:
                runway = int(cash_bal / net_burn)

            period_label = f'{calendar.month_abbr[cons_latest.period_month]}-{cons_latest.period_year}' if cons_latest.period_month else str(cons_latest.period_year)

            burn_runway.append({
                'company': f'{fund.name} (Fund-Level)',
                'gross_burn': gross_burn,
                'net_burn': net_burn if net_burn and net_burn > 0 else None,
                'cash_balance': cash_bal,
                'runway_months': runway,
                'period': period_label,
            })

    data['burn_runway'] = burn_runway

    # ── SaaS Metrics (from PortfolioKPI) ─────────────────────────────
    SAAS_SLUGS = {
        'mrr': ['mrr', 'monthly-recurring-revenue', 'monthly-revenue'],
        'arr': ['arr', 'annual-recurring-revenue', 'annual-revenue-run-rate'],
        'churn_rate': ['churn-rate', 'churn', 'revenue-churn', 'customer-churn'],
        'nrr': ['nrr', 'net-revenue-retention', 'net-dollar-retention', 'ndr'],
        'cac': ['cac', 'customer-acquisition-cost', 'blended-cac'],
        'ltv': ['ltv', 'clv', 'customer-ltv', 'lifetime-value', 'customer-lifetime-value'],
        'ltv_cac_ratio': ['ltv-cac', 'ltv-cac-ratio', 'ltv-cac-multiple'],
    }
    slug_to_saas = {}
    for sk, slugs in SAAS_SLUGS.items():
        for s in slugs:
            slug_to_saas[s] = sk

    saas_kpis = PortfolioKPI.objects.filter(
        investment__scheme__fund=fund
    ).select_related('kpi_definition', 'investment__portfolio_company').order_by(
        'investment__portfolio_company__name', '-period'
    )

    co_saas = defaultdict(lambda: {k: None for k in SAAS_SLUGS})
    for kpi in saas_kpis:
        slug = kpi.kpi_definition.slug.lower()
        saas_key = None
        for pattern, sk in slug_to_saas.items():
            if pattern in slug:
                saas_key = sk
                break
        if not saas_key:
            continue
        co_name = kpi.investment.portfolio_company.name if kpi.investment.portfolio_company else kpi.investment.company_name
        if co_saas[co_name][saas_key] is None:
            co_saas[co_name][saas_key] = float(kpi.value) if kpi.value else None

    saas_metrics = []
    for co_name, vals in sorted(co_saas.items()):
        if any(v is not None for v in vals.values()):
            saas_metrics.append({
                'company': co_name,
                **vals,
            })
    data['saas_metrics'] = saas_metrics

    # ── KPI Matrix (company × KPI columns — GMV, REV, Gross M%, etc.) ──
    _KPI_COL_SLUGS = {
        'gmv':      ['gmv', 'gmv-rs-cr', 'gmvcr'],
        'revenue':  ['rev', 'revenue', 'revenue-rs-cr', 'revenuecr'],
        'gross_m':  ['gross-m', 'gross-margin'],
        'ebitda':   ['ebitda', 'ebitda-margin'],
        'orders':   ['orders', 'order-book', 'order-book-rs-cr'],
        'aov':      ['aov'],
        'returns':  ['returns'],
        'cac':      ['cac'],
        'repeat':   ['repeat'],
    }
    _SLUG_TO_COL = {}
    for _col, _slugs in _KPI_COL_SLUGS.items():
        for _s in _slugs:
            _SLUG_TO_COL[_s] = _col

    # Build company data from investments
    company_data = {}
    for inv in investments:
        co_name = inv.company_name
        if co_name not in company_data:
            company_data[co_name] = {
                'inv_ids': [],
                'cost': float(inv.total_invested or 0),
                'sector': inv.sector or '—',
            }
        else:
            company_data[co_name]['cost'] += float(inv.total_invested or 0)
        company_data[co_name]['inv_ids'].append(inv.id)

    # Get FV per company
    for co_name, cd in company_data.items():
        total_fv_co = 0
        for inv_id in cd['inv_ids']:
            latest_v = Valuation.objects.filter(
                investment_id=inv_id, status='approved'
            ).order_by('-valuation_date').first()
            if not latest_v:
                latest_v = Valuation.objects.filter(
                    investment_id=inv_id
                ).order_by('-valuation_date').first()
            if latest_v and latest_v.fair_value_of_holding:
                total_fv_co += float(latest_v.fair_value_of_holding)
        cd['fv'] = total_fv_co

    # Get KPIs matching target slugs
    all_inv_ids = [inv.id for inv in investments]
    target_slugs = set()
    for slugs in _KPI_COL_SLUGS.values():
        target_slugs.update(slugs)

    kpi_qs = PortfolioKPI.objects.filter(
        investment_id__in=all_inv_ids,
        kpi_definition__slug__in=target_slugs
    ).select_related('kpi_definition', 'investment').order_by('-period')

    company_kpis = defaultdict(dict)
    for kpi in kpi_qs:
        co_name = kpi.investment.company_name
        slug = kpi.kpi_definition.slug.lower()
        col = _SLUG_TO_COL.get(slug)
        if col and col not in company_kpis[co_name]:
            company_kpis[co_name][col] = float(kpi.value) if kpi.value else None

    # Broader fuzzy matching
    all_kpis = PortfolioKPI.objects.filter(
        investment_id__in=all_inv_ids
    ).select_related('kpi_definition', 'investment').order_by('-period')

    for kpi in all_kpis:
        co_name = kpi.investment.company_name
        slug = kpi.kpi_definition.slug.lower()
        for col, patterns in _KPI_COL_SLUGS.items():
            if col not in company_kpis[co_name]:
                for p in patterns:
                    if p in slug:
                        company_kpis[co_name][col] = float(kpi.value) if kpi.value else None
                        break

    # Normalize percentages
    PCT_COLS = {'gross_m', 'ebitda', 'returns', 'repeat'}
    for co_name, kvals in company_kpis.items():
        for pc in PCT_COLS:
            v = kvals.get(pc)
            if v is not None and abs(v) < 1:
                kvals[pc] = round(v * 100, 2)

    kpi_matrix = []
    for co_name in sorted(company_data.keys()):
        cd = company_data[co_name]
        kvals = company_kpis.get(co_name, {})
        kpi_matrix.append({
            'company': co_name,
            'gmv': kvals.get('gmv'),
            'revenue': kvals.get('revenue'),
            'gross_m': kvals.get('gross_m'),
            'ebitda': kvals.get('ebitda'),
            'orders': kvals.get('orders'),
            'aov': kvals.get('aov'),
            'returns': kvals.get('returns'),
            'cac': kvals.get('cac'),
            'repeat': kvals.get('repeat'),
            'cost': cd.get('cost'),
            'fv': cd.get('fv'),
        })
    data['kpi_matrix'] = kpi_matrix

    # ── Quoted vs Unquoted ───────────────────────────────────────────
    quoted = []
    unquoted = []
    for inv in investments:
        co = inv.portfolio_company
        is_quoted = getattr(co, 'is_quoted', False)
        latest_v = inv.valuations.order_by('-valuation_date').first()
        ipev_level = latest_v.ipev_level if latest_v and hasattr(latest_v, 'ipev_level') else None
        entry = {
            'name': co.name,
            'sector': co.sector if hasattr(co, 'sector') and co.sector else '—',
            'exchange': getattr(co, 'exchange', '') or '',
            'cost': float(inv.total_invested or 0),
            'fair_value': float(latest_v.fair_value_of_holding) if latest_v and latest_v.fair_value_of_holding else 0,
            'ipev_level': ipev_level or '—',
        }
        if is_quoted or ipev_level == 1:
            quoted.append(entry)
        else:
            unquoted.append(entry)
    data['quoted_companies'] = quoted
    data['unquoted_companies'] = unquoted
    data['qu_summary'] = {
        'total': len(quoted) + len(unquoted),
        'quoted_count': len(quoted),
        'unquoted_count': len(unquoted),
        'quoted_cost': sum(q.get('cost', 0) for q in quoted),
        'unquoted_cost': sum(q.get('cost', 0) for q in unquoted),
    }

    # ── Investors ────────────────────────────────────────────────────
    investors_qs = Investor.objects.filter(organization=org)
    commitments = Commitment.objects.filter(scheme__fund=fund).select_related('investor')
    data['investor_count'] = investors_qs.count()

    investor_data = []
    for c in commitments:
        commitment_amt = float(c.commitment_amount or 0)
        investor_data.append({
            'name': c.investor.investor_name if c.investor else '—',
            'type': c.investor.investor_type if c.investor else '—',
            'commitment': commitment_amt,
            'drawn': 0,
            'remaining': commitment_amt,
            'status': c.commitment_status if hasattr(c, 'commitment_status') else 'Active',
        })
    data['investors'] = investor_data

    # ── Capital Calls ────────────────────────────────────────────────
    capital_calls = CapitalCall.objects.filter(
        scheme__fund=fund
    ).order_by('-call_date')[:30]
    data['capital_calls'] = [{
        'call_number': c.call_number,
        'date': str(c.call_date) if c.call_date else '—',
        'amount': float(c.total_call_amount or 0),
        'purpose': c.purpose if hasattr(c, 'purpose') else '—',
        'status': c.call_status,
    } for c in capital_calls]

    # ── Distributions ────────────────────────────────────────────────
    distributions = Distribution.objects.filter(
        scheme__fund=fund
    ).order_by('-distribution_date')[:30]
    data['distributions'] = [{
        'date': str(d.distribution_date) if d.distribution_date else '—',
        'amount': float(d.total_gross_amount or 0),
        'type': d.distribution_type or '—',
    } for d in distributions]

    # ── LP Capital Accounts ──────────────────────────────────────────
    lp_accounts = LPCapitalAccount.objects.filter(
        commitment__scheme__fund=fund
    ).select_related('commitment__investor')[:30]
    data['lp_accounts'] = [{
        'investor': lp.commitment.investor.investor_name if lp.commitment and lp.commitment.investor else '—',
        'committed': float(lp.committed_capital) if lp.committed_capital else 0,
        'called': float(lp.called_capital) if lp.called_capital else 0,
        'distributed': float(lp.distributed_capital) if lp.distributed_capital else 0,
        'net_balance': float((lp.called_capital or 0) - (lp.distributed_capital or 0)),
        'dpi': float(lp.dpi) if lp.dpi else 0,
    } for lp in lp_accounts]

    # ── Fund Ledger (recent entries) ─────────────────────────────────
    ledger_entries = []
    for scheme in schemes:
        entries = FundLedger.objects.filter(
            scheme=scheme, is_reversed=False
        ).select_related('debit_account', 'credit_account').order_by('-entry_date')[:30]
        for e in entries:
            ledger_entries.append({
                'je_number': e.journal_entry_number or '—',
                'date': str(e.entry_date) if e.entry_date else '—',
                'description': e.description or '—',
                'debit_account': e.debit_account.account_name if e.debit_account else '—',
                'credit_account': e.credit_account.account_name if e.credit_account else '—',
                'amount': float(e.amount or 0),
                'reference_type': e.reference_type or '—',
            })
    ledger_entries.sort(key=lambda x: x['date'], reverse=True)
    data['ledger_entries'] = ledger_entries[:40]

    # ── Trial Balance (for first scheme) ─────────────────────────────
    trial_balance = {}
    if schemes.exists():
        try:
            from accounting.trial_balance import generate_trial_balance
            tb = generate_trial_balance(schemes.first())
            trial_balance = tb
        except Exception as e:
            logger.warning('Trial balance generation failed: %s', e)
    data['trial_balance'] = trial_balance

    # ── Sector breakdown ─────────────────────────────────────────────
    sector_map = defaultdict(lambda: {'count': 0, 'invested': 0, 'fair_value': 0})
    for v in valuations:
        sec = v.get('sector', 'Other')
        sector_map[sec]['count'] += 1
        sector_map[sec]['invested'] += v.get('invested', 0)
        sector_map[sec]['fair_value'] += v.get('fair_value', 0)

    data['sector_breakdown'] = [{
        'sector': sec,
        'count': info['count'],
        'invested': info['invested'],
        'fair_value': info['fair_value'],
        'avg_moic': round(info['fair_value'] / info['invested'], 2) if info['invested'] > 0 else 0,
    } for sec, info in sorted(sector_map.items())]

    # ── Anomaly alerts ───────────────────────────────────────────────
    anomalies = MISAnomalyAlert.objects.filter(
        portfolio_company__organization=org, resolved=False
    ).select_related('portfolio_company').order_by('-severity', '-detected_at')[:20]
    data['anomaly_count'] = anomalies.count()
    data['anomalies'] = [{
        'company': a.portfolio_company.name,
        'type': a.get_anomaly_type_display(),
        'severity': a.severity.upper(),
        'description': a.description[:80],
        'detected': str(a.detected_at.date()),
    } for a in anomalies]

    return data


