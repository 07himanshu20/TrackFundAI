def _generate_executive_summary(data):
    parts = []
    company_count = data.get('company_count', 0)
    aum = data.get('latest_aum')
    total_inv = data.get('total_invested')
    total_fv = data.get('total_fv')
    moic = data.get('aggregate_moic')

    if aum:
        parts.append(
            f'The fund manages an AUM of {_fmt_cr(aum)} '
            f'across {company_count} active portfolio companies.'
        )
    else:
        parts.append(f'The fund has {company_count} active portfolio companies.')

    if total_inv and total_fv:
        parts.append(
            f'Capital deployed: {_fmt_cr(total_inv)}, current fair value: '
            f'{_fmt_cr(total_fv)}, aggregate MOIC: {moic:.2f}x.'
        )

    anomaly_count = data.get('anomaly_count', 0)
    if anomaly_count > 0:
        parts.append(f'{anomaly_count} active anomaly alerts require attention.')
    else:
        parts.append('All companies within expected variance thresholds.')

    rev = data.get('agg_revenue')
    ebitda = data.get('agg_ebitda')
    if rev:
        parts.append(
            f'Aggregate portfolio revenue: {_fmt_cr(rev)}'
            + (f', EBITDA: {_fmt_cr(ebitda)}.' if ebitda else '.')
        )

    return ' '.join(parts) if parts else 'Executive summary will be populated once fund data is imported.'


def _generate_lp_letter(data, fund, report_date_str):
    """Generate a Quarterly LP Letter from fund data."""
    import calendar
    from datetime import date

    today = date.today()
    quarter = (today.month - 1) // 3 + 1
    quarter_end_month = quarter * 3
    quarter_label = f'Q{quarter} {today.year}'

    parts = []
    parts.append(f'<b>Dear Limited Partners,</b>')
    parts.append(f'<br/><br/>')
    parts.append(
        f'We are pleased to present the quarterly update for <b>{fund.name}</b> '
        f'for the period ending {report_date_str}.'
    )
    parts.append(f'<br/><br/>')

    # Fund performance summary
    aum = data.get('latest_aum')
    total_inv = data.get('total_invested')
    total_fv = data.get('total_fv')
    moic = data.get('aggregate_moic', 0)
    company_count = data.get('company_count', 0)

    parts.append(f'<b>Fund Performance:</b><br/>')
    if aum:
        parts.append(f'The fund currently manages an AUM of {_fmt_cr(aum)}. ')
    parts.append(
        f'As of this reporting date, the fund has deployed capital across '
        f'{company_count} portfolio companies. '
    )
    if total_inv and total_fv:
        parts.append(
            f'Total capital deployed stands at {_fmt_cr(total_inv)} with a current portfolio '
            f'fair value of {_fmt_cr(total_fv)}, translating to a gross MOIC of {moic:.2f}x. '
        )
    parts.append(f'<br/><br/>')

    # Portfolio highlights
    rev = data.get('agg_revenue')
    ebitda = data.get('agg_ebitda')
    pat = data.get('agg_pat')

    parts.append(f'<b>Portfolio Highlights:</b><br/>')
    if rev:
        parts.append(f'Aggregate portfolio revenue for the latest period: {_fmt_cr(rev)}. ')
    if ebitda:
        parts.append(f'Aggregate EBITDA: {_fmt_cr(ebitda)}. ')
    if pat:
        parts.append(f'Aggregate PAT: {_fmt_cr(pat)}. ')

    # Top performers
    pl = data.get('pl_consolidated', [])
    if pl:
        top = sorted([p for p in pl if p.get('revenue')], key=lambda x: x['revenue'] or 0, reverse=True)[:3]
        if top:
            names = ', '.join(t['company'] for t in top)
            parts.append(f'Top revenue contributors: {names}. ')
    parts.append(f'<br/><br/>')

    # Capital account
    capital_calls = data.get('capital_calls', [])
    distributions_data = data.get('distributions', [])
    parts.append(f'<b>Capital Activity:</b><br/>')
    total_called = sum(c.get('amount', 0) for c in capital_calls)
    total_distributed = sum(d.get('amount', 0) for d in distributions_data)
    parts.append(
        f'Total capital called to date: {_fmt_cr(total_called)}. '
        f'Total distributions to date: {_fmt_cr(total_distributed)}. '
    )
    investor_count = data.get('investor_count', 0)
    if investor_count:
        parts.append(f'The fund currently has {investor_count} investors. ')
    parts.append(f'<br/><br/>')

    # NAV
    nav_per_unit = data.get('nav_per_unit')
    if nav_per_unit:
        parts.append(f'<b>NAV Update:</b><br/>')
        parts.append(f'The latest NAV per unit stands at Rs.{float(nav_per_unit):,.2f}. ')
        parts.append(f'<br/><br/>')

    # Risk flags
    anomaly_count = data.get('anomaly_count', 0)
    parts.append(f'<b>Risk & Compliance:</b><br/>')
    if anomaly_count > 0:
        parts.append(f'{anomaly_count} anomaly alerts have been flagged and are under review. ')
    else:
        parts.append(
            'All portfolio companies are operating within expected variance thresholds. '
            'No material risk flags to report. '
        )
    parts.append(f'<br/><br/>')

    # Closing
    parts.append(
        'We remain committed to maximising value for our investors and will continue '
        'to actively engage with portfolio companies to drive operational improvements '
        'and growth. We look forward to sharing further updates in the next quarter.'
    )
    parts.append(f'<br/><br/>')
    parts.append(f'Warm regards,<br/>')
    parts.append(f'<b>Fund Management Team</b><br/>')
    parts.append(f'{fund.name}')

    return ''.join(parts)


