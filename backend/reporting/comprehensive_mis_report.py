def _collect_all_data(fund, org):
    """Collect all data needed for the comprehensive MIS report."""
    from investments.models import PortfolioCompany, Investment, Valuation, PortfolioKPI, KPIDefinition
    from accounting.models import NAVRecord, CarriedInterest, FundLedger
    from lp.models import Investor, Commitment, CapitalCall, Distribution, LPCapitalAccount
    from mis_consolidation.models import BudgetVsActual, ConsolidatedMIS, MISAnomalyAlert
    from funds.models import Scheme

    import calendar

    data = {}
    schemes = Scheme.objects.filter(fund=fund)
    data['scheme_names'] = [s.name for s in schemes]
    data['vintage_year'] = schemes.first().vintage_year if schemes.exists() and hasattr(schemes.first(), 'vintage_year') else None
    data['hurdle_rate'] = schemes.first().hurdle_rate_pct if schemes.exists() and hasattr(schemes.first(), 'hurdle_rate_pct') else None
    data['carry_pct'] = schemes.first().carry_pct if schemes.exists() and hasattr(schemes.first(), 'carry_pct') else None

    # Companies
    companies = PortfolioCompany.objects.filter(organization=org, is_active=True)
    data['company_count'] = companies.count()

    # Investments
    investments = Investment.objects.filter(
        scheme__fund=fund, status__in=['active', 'partially_exited']
    ).select_related('portfolio_company', 'scheme')

    total_invested = sum(float(i.total_invested or 0) for i in investments)
    data['total_invested'] = total_invested

    # ── Investments list (for Investments Summary table) ──
    investments_list = []
    for inv in investments:
        investments_list.append({
            'company': inv.company_name[:30],
            'scheme': inv.scheme.name if inv.scheme else '—',
            'sector': inv.sector or '—',
            'stage': getattr(inv, 'stage', '—') or '—',
            'total_invested': float(inv.total_invested or 0),
            'ownership_pct': float(inv.ownership_pct) if hasattr(inv, 'ownership_pct') and inv.ownership_pct else None,
            'status': inv.status,
            'investment_date': str(inv.investment_date) if hasattr(inv, 'investment_date') and inv.investment_date else '—',
        })
    data['investments_list'] = investments_list

    # Latest valuations per investment
    valuations = []
    total_fv = 0
    for inv in investments:
        latest_val = inv.valuations.filter(status='approved').order_by('-valuation_date').first()
        if not latest_val:
            latest_val = inv.valuations.order_by('-valuation_date').first()
        fv = float(latest_val.fair_value_of_holding) if latest_val and latest_val.fair_value_of_holding else 0
        total_fv += fv
        invested = float(inv.total_invested or 0)
        moic = fv / invested if invested > 0 else 0
        valuations.append({
            'company': inv.company_name[:30],
            'sector': inv.sector or '—',
            'invested': invested,
            'fair_value': fv,
            'moic': round(moic, 2),
            'methodology': latest_val.methodology if latest_val else '—',
            'date': str(latest_val.valuation_date) if latest_val else '—',
        })
    data['valuations'] = valuations
    data['total_fv'] = total_fv
    data['aggregate_moic'] = round(total_fv / total_invested, 2) if total_invested > 0 else 0

    # NAV records
    nav_records = NAVRecord.objects.filter(
        scheme__fund=fund
    ).select_related('scheme').order_by('-nav_date')[:24]

    data['nav_records'] = [{
        'date': str(n.nav_date),
        'total_nav': float(n.total_nav),
        'nav_per_unit': float(n.nav_per_unit),
        'total_units': float(n.total_units_outstanding),
        'mgmt_fee': float(n.management_fee_payable),
        'scheme': n.scheme.name if n.scheme else '—',
    } for n in nav_records]

    data['latest_aum'] = float(nav_records[0].total_nav) if nav_records else None
    data['nav_per_unit'] = float(nav_records[0].nav_per_unit) if nav_records else None

    # Carried Interest
    carry_records = CarriedInterest.objects.filter(
        scheme__fund=fund
    ).select_related('scheme').order_by('-calculation_date')[:12]
    data['carry_records'] = [{
        'scheme': c.scheme.name if c.scheme else '—',
        'date': str(c.calculation_date),
        'carry_base': float(c.carry_base),
        'carry_gross': float(c.carry_amount_gross),
        'carry_net': float(c.carry_amount_net),
        'clawback': float(c.gp_clawback_provision),
    } for c in carry_records]

    # ── BudgetVsActual — build consolidated P&L (dashboard-style) ────
    line_item_display = dict(BudgetVsActual._meta.get_field('line_item').choices)

    bva_qs = BudgetVsActual.objects.filter(
        portfolio_company__organization=org, fund=fund
    ).select_related('portfolio_company').order_by(
        'portfolio_company__name', '-period_year', '-period_month', 'line_item'
    )

    # Group BvA by company → get latest period per company → extract rev/ebitda/pat
    company_bva = defaultdict(list)
    for r in bva_qs:
        company_bva[r.portfolio_company.name].append(r)

    pl_consolidated = []
    bva_summary = []
    agg_revenue = Decimal('0')
    agg_ebitda = Decimal('0')
    agg_pat = Decimal('0')

    for co_name, records in company_bva.items():
        # Find latest period
        latest_year = max(r.period_year for r in records)
        latest_month_records = [r for r in records if r.period_year == latest_year]
        months = [r.period_month for r in latest_month_records if r.period_month]
        latest_month = max(months) if months else None
        if latest_month:
            period_records = [r for r in latest_month_records if r.period_month == latest_month]
        else:
            period_records = latest_month_records

        rev = None
        ebitda = None
        pat = None
        for r in period_records:
            if r.line_item in ('revenue', 'total_revenue') and r.actual_inr:
                rev = float(r.actual_inr)
            elif r.line_item == 'ebitda' and r.actual_inr:
                ebitda = float(r.actual_inr)
            elif r.line_item == 'pat' and r.actual_inr:
                pat = float(r.actual_inr)

        ebitda_pct = None
        if rev and ebitda and rev != 0:
            ebitda_pct = ebitda / rev * 100

        if latest_month:
            period_label = f'{calendar.month_abbr[latest_month]}-{latest_year}'
        else:
            period_label = str(latest_year)

        pl_consolidated.append({
            'company': co_name,
            'revenue': rev,
            'ebitda': ebitda,
            'pat': pat,
            'ebitda_pct': ebitda_pct,
            'period': period_label,
        })

        if rev:
            agg_revenue += Decimal(str(rev))
        if ebitda:
            agg_ebitda += Decimal(str(ebitda))
        if pat:
            agg_pat += Decimal(str(pat))

        # BvA detail records (for variance table)
        for r in records:
            if r.budget_inr is not None and r.actual_inr is not None:
                bva_summary.append({
                    'company': co_name,
                    'line_item': line_item_display.get(r.line_item, r.line_item),
                    'budget': float(r.budget_inr),
                    'actual': float(r.actual_inr),
                    'variance': float(r.actual_inr - r.budget_inr),
                    'variance_pct': float(r.variance_pct) if r.variance_pct else None,
                    'is_favorable': r.is_favorable,
                })

    # ── Fallback: if no BvA P&L, synthesize from PortfolioKPI ────────
    if not pl_consolidated:
        KPI_PL_SLUGS = {
            'revenue': ['revenue', 'rev', 'net-sales', 'revcr', 'rev-cr', 'revenue-rs-cr', 'revenuecr'],
            'ebitda': ['ebitda', 'ebitda-cr', 'ebitdacr', 'ebitda-margin'],
            'pat': ['pat', 'net-profit', 'net-income', 'profit-after-tax'],
        }
        slug_to_pl = {}
        for pl_key, slugs in KPI_PL_SLUGS.items():
            for s in slugs:
                slug_to_pl[s] = pl_key

        kpi_qs = PortfolioKPI.objects.filter(
            investment__scheme__fund=fund
        ).select_related('kpi_definition', 'investment__portfolio_company').order_by(
            'investment__portfolio_company__name', '-period'
        )

        co_kpi_pl = defaultdict(lambda: {'revenue': None, 'ebitda': None, 'pat': None, 'period': None})
        for kpi in kpi_qs:
            slug = kpi.kpi_definition.slug.lower()
            pl_key = None
            for pattern, pk in slug_to_pl.items():
                if pattern in slug:
                    pl_key = pk
                    break
            if not pl_key:
                continue
            co_name = kpi.investment.portfolio_company.name if kpi.investment.portfolio_company else kpi.investment.company_name
            if co_kpi_pl[co_name][pl_key] is None:
                co_kpi_pl[co_name][pl_key] = float(kpi.value) if kpi.value else None
                if co_kpi_pl[co_name]['period'] is None:
                    if kpi.period:
                        co_kpi_pl[co_name]['period'] = f'{calendar.month_abbr[kpi.period.month]}-{kpi.period.year}'
                    else:
                        co_kpi_pl[co_name]['period'] = '—'

        for co_name, vals in co_kpi_pl.items():
            rev = vals.get('revenue')
            ebitda_raw = vals.get('ebitda')
            pat = vals.get('pat')

            # If ebitda looks like a margin (value < 1), treat as percentage
            # and compute absolute EBITDA from revenue × margin
            ebitda = ebitda_raw
            ebitda_pct = None
            if ebitda_raw is not None and abs(ebitda_raw) < 1:
                # It's a margin, not an absolute value
                ebitda_pct = ebitda_raw * 100
                if rev and rev > 0:
                    ebitda = rev * ebitda_raw  # absolute EBITDA
                else:
                    ebitda = None  # can't compute absolute without revenue
            elif rev and ebitda and rev != 0:
                ebitda_pct = ebitda / rev * 100

            pl_consolidated.append({
                'company': co_name,
                'revenue': rev,
                'ebitda': round(ebitda, 2) if ebitda is not None else None,
                'pat': pat,
                'ebitda_pct': round(ebitda_pct, 1) if ebitda_pct is not None else None,
                'period': vals.get('period', '—'),
            })
            if rev:
                agg_revenue += Decimal(str(rev))
            if ebitda:
                agg_ebitda += Decimal(str(round(ebitda, 2)))
            if pat:
                agg_pat += Decimal(str(pat))

    # Sort by revenue descending
    pl_consolidated.sort(key=lambda x: x.get('revenue') or 0, reverse=True)
    data['pl_consolidated'] = pl_consolidated

    # Fallback: if no BvA detail, build from ConsolidatedMIS actuals vs budgets
    if not bva_summary:
        cons_bva_qs = ConsolidatedMIS.objects.filter(
            fund=fund, organization=org
        ).order_by('-period_year', '-period_month', 'line_item')

        # Get the latest period
        latest_cons = cons_bva_qs.first()
        if latest_cons:
            latest_yr = latest_cons.period_year
            latest_mo = latest_cons.period_month
            period_cons = cons_bva_qs.filter(period_year=latest_yr, period_month=latest_mo)
            for c in period_cons:
                actual = float(c.total_actual_inr) if c.total_actual_inr else 0
                budget = float(c.total_budget_inr) if c.total_budget_inr else 0
                variance = actual - budget
                variance_pct = (variance / budget * 100) if budget != 0 else None
                is_fav = None
                if c.line_item in ('revenue', 'total_revenue', 'gross_profit', 'ebitda', 'ebit', 'pbt', 'pat', 'other_income'):
                    is_fav = variance >= 0
                elif c.line_item in ('cogs', 'total_opex', 'depreciation'):
                    is_fav = variance <= 0
                period_label = f'{calendar.month_abbr[latest_mo]}-{latest_yr}' if latest_mo else str(latest_yr)
                bva_summary.append({
                    'company': f'{fund.name} (Fund)',
                    'line_item': c.line_item.replace('_', ' ').title(),
                    'budget': budget,
                    'actual': actual,
                    'variance': variance,
                    'variance_pct': round(variance_pct, 1) if variance_pct is not None else None,
                    'is_favorable': is_fav,
                })

    data['bva_summary'] = bva_summary[:80]
    data['agg_revenue'] = float(agg_revenue) if agg_revenue else None
    data['agg_ebitda'] = float(agg_ebitda) if agg_ebitda else None
    data['agg_pat'] = float(agg_pat) if agg_pat else None

    # ── Consolidated Fund P&L (fund-level monthly rollup) ────────────
    consolidated_qs = ConsolidatedMIS.objects.filter(
        fund=fund, organization=org
    ).order_by('-period_year', '-period_month')

    fund_pl_map = defaultdict(lambda: {'revenue': None, 'ebitda': None, 'pat': None, 'company_count': 0})
    for c in consolidated_qs:
        key = f'{c.period_year}-{c.period_month or "Annual"}'
        if c.line_item in ('revenue', 'total_revenue') and fund_pl_map[key]['revenue'] is None:
            fund_pl_map[key]['revenue'] = float(c.total_actual_inr) if c.total_actual_inr else None
            fund_pl_map[key]['company_count'] = c.company_count or 0
        elif c.line_item == 'ebitda' and fund_pl_map[key]['ebitda'] is None:
            fund_pl_map[key]['ebitda'] = float(c.total_actual_inr) if c.total_actual_inr else None
        elif c.line_item == 'pat' and fund_pl_map[key]['pat'] is None:
            fund_pl_map[key]['pat'] = float(c.total_actual_inr) if c.total_actual_inr else None

    fund_pl = []
    for period_key in sorted(fund_pl_map.keys(), reverse=True)[:12]:
        vals = fund_pl_map[period_key]
        parts = period_key.split('-')
        if len(parts) == 2 and parts[1] != 'Annual':
            try:
                period_label = f'{calendar.month_abbr[int(parts[1])]}-{parts[0]}'
            except (ValueError, IndexError):
                period_label = period_key
        else:
            period_label = period_key
        rev = vals.get('revenue')
        ebitda = vals.get('ebitda')
        ebitda_pct = None
        if rev and ebitda and rev != 0:
            ebitda_pct = ebitda / rev * 100
        fund_pl.append({
            'period': period_label,
            'revenue': rev,
            'ebitda': ebitda,
            'pat': vals.get('pat'),
            'ebitda_pct': ebitda_pct,
            'company_count': vals.get('company_count', 0),
        })
    data['fund_pl'] = fund_pl

    # ── Balance Sheet (consolidated — one row per company) ───────────
    BS_ITEMS = {'total_assets', 'net_worth', 'total_debt', 'cash_and_equivalents'}

    bs_bva = BudgetVsActual.objects.filter(
        portfolio_company__organization=org, fund=fund,
        line_item__in=BS_ITEMS
    ).select_related('portfolio_company').order_by(
        'portfolio_company__name', '-period_year', '-period_month'
    )

    bs_co = defaultdict(dict)
    for r in bs_bva:
        co_name = r.portfolio_company.name
        if r.line_item not in bs_co[co_name]:
            bs_co[co_name][r.line_item] = {
                'value': float(r.actual_inr) if r.actual_inr else None,
                'period': f'{r.period_year}-{r.period_month or "Annual"}',
            }

    bs_consolidated = []
    for co_name, items in sorted(bs_co.items()):
        period = next((v['period'] for v in items.values() if v.get('period')), '—')
        parts = period.split('-')
        if len(parts) == 2 and parts[1] != 'Annual':
            try:
                period = f'{calendar.month_abbr[int(parts[1])]}-{parts[0]}'
            except (ValueError, IndexError):
                pass
        bs_consolidated.append({
            'company': co_name,
            'total_assets': items.get('total_assets', {}).get('value'),
            'net_worth': items.get('net_worth', {}).get('value'),
            'total_debt': items.get('total_debt', {}).get('value'),
            'cash': items.get('cash_and_equivalents', {}).get('value'),
            'period': period,
        })

    # Fallback: build fund-level Balance Sheet from NAVRecord if BvA BS is empty
    if not bs_consolidated:
        latest_nav = NAVRecord.objects.filter(
            scheme__fund=fund
        ).order_by('-nav_date').first()
        if latest_nav:
            nav_date_str = f'{calendar.month_abbr[latest_nav.nav_date.month]}-{latest_nav.nav_date.year}'
            total_assets = float(latest_nav.investments_at_fair_value or 0) + \
                           float(latest_nav.cash_and_equivalents or 0) + \
                           float(latest_nav.receivables or 0)
            total_liabilities = float(latest_nav.management_fee_payable or 0) + \
                                float(latest_nav.other_liabilities or 0)
            net_worth = total_assets - total_liabilities
            bs_consolidated.append({
                'company': f'{fund.name} (Fund-Level)',
                'total_assets': total_assets,
                'net_worth': net_worth,
                'total_debt': total_liabilities,
                'cash': float(latest_nav.cash_and_equivalents or 0),
                'period': nav_date_str,
            })
            # Also add a detailed breakdown as separate rows
            bs_consolidated.append({
                'company': '  Investments at Fair Value',
                'total_assets': float(latest_nav.investments_at_fair_value or 0),
                'net_worth': None,
                'total_debt': None,
                'cash': None,
                'period': nav_date_str,
            })
            bs_consolidated.append({
                'company': '  Receivables',
                'total_assets': float(latest_nav.receivables or 0),
                'net_worth': None,
                'total_debt': None,
                'cash': None,
                'period': nav_date_str,
            })
            bs_consolidated.append({
                'company': '  Mgmt Fee Payable',
                'total_assets': None,
                'net_worth': None,
                'total_debt': float(latest_nav.management_fee_payable or 0),
                'cash': None,
                'period': nav_date_str,
            })
            bs_consolidated.append({
                'company': '  Other Liabilities',
                'total_assets': None,
                'net_worth': None,
                'total_debt': float(latest_nav.other_liabilities or 0),
                'cash': None,
                'period': nav_date_str,
            })
            bs_consolidated.append({
                'company': '  Total NAV',
                'total_assets': float(latest_nav.total_nav or 0),
                'net_worth': float(latest_nav.total_nav or 0),
                'total_debt': None,
                'cash': None,
                'period': nav_date_str,
            })

    data['bs_consolidated'] = bs_consolidated

    # ── Cash Flow (consolidated — one row per company) ───────────────
    CF_ITEMS = {'cash_and_equivalents', 'finance_cost', 'capex', 'dividend'}

    cf_bva = BudgetVsActual.objects.filter(
        portfolio_company__organization=org, fund=fund,
        line_item__in=CF_ITEMS
    ).select_related('portfolio_company').order_by(
        'portfolio_company__name', '-period_year', '-period_month'
    )

    cf_co = defaultdict(dict)
    for r in cf_bva:
        co_name = r.portfolio_company.name
        if r.line_item not in cf_co[co_name]:
            cf_co[co_name][r.line_item] = {
                'value': float(r.actual_inr) if r.actual_inr else None,
                'period': f'{r.period_year}-{r.period_month or "Annual"}',
            }

    cf_consolidated = []
    for co_name, items in sorted(cf_co.items()):
        period = next((v['period'] for v in items.values() if v.get('period')), '—')
        parts = period.split('-')
        if len(parts) == 2 and parts[1] != 'Annual':
            try:
                period = f'{calendar.month_abbr[int(parts[1])]}-{parts[0]}'
            except (ValueError, IndexError):
                pass
        cf_consolidated.append({
            'company': co_name,
            'cash': items.get('cash_and_equivalents', {}).get('value'),
            'finance_cost': items.get('finance_cost', {}).get('value'),
            'capex': items.get('capex', {}).get('value'),
            'dividend': items.get('dividend', {}).get('value'),
            'period': period,
        })

    # Fallback: build fund-level Cash Flow from FundLedger if BvA CF is empty
    if not cf_consolidated:
        ledger_qs = FundLedger.objects.filter(
            scheme__fund=fund, is_reversed=False
        ).order_by('-entry_date')

        if ledger_qs.exists():
            # Group by reference_type and sum amounts
            from django.db.models import Sum
            cf_by_type = ledger_qs.values('reference_type').annotate(
                total=Sum('amount')
            ).order_by('reference_type')

            cf_type_totals = {}
            for row in cf_by_type:
                cf_type_totals[row['reference_type']] = float(row['total'] or 0)

            latest_entry = ledger_qs.first()
            cf_period = f'{calendar.month_abbr[latest_entry.entry_date.month]}-{latest_entry.entry_date.year}' if latest_entry else '—'

            capital_inflows = cf_type_totals.get('capital_call', 0)
            investment_outflows = cf_type_totals.get('investment', 0)
            distributions_out = cf_type_totals.get('distribution', 0)
            mgmt_fees = cf_type_totals.get('management_fee', 0)
            carry = cf_type_totals.get('carried_interest', 0)
            expenses = cf_type_totals.get('expense', 0)
            other = cf_type_totals.get('other', 0)

            # Operating: mgmt fees + expenses
            operating_cf = -(mgmt_fees + expenses)
            # Investing: investments made (outflow)
            investing_cf = -investment_outflows
            # Financing: capital calls (inflow) - distributions (outflow) - carry
            financing_cf = capital_inflows - distributions_out - carry

            net_cf = operating_cf + investing_cf + financing_cf

            # Get ending cash from latest NAVRecord
            latest_nav = NAVRecord.objects.filter(scheme__fund=fund).order_by('-nav_date').first()
            ending_cash = float(latest_nav.cash_and_equivalents or 0) if latest_nav else None

            data['cf_fund_level'] = True
            cf_consolidated = [
                {
                    'item': 'Capital Calls Received',
                    'amount': capital_inflows,
                    'category': 'Financing',
                },
                {
                    'item': 'Investments Made',
                    'amount': -investment_outflows,
                    'category': 'Investing',
                },
                {
                    'item': 'Distributions Paid',
                    'amount': -distributions_out,
                    'category': 'Financing',
                },
                {
                    'item': 'Management Fees',
                    'amount': -mgmt_fees,
                    'category': 'Operating',
                },
                {
                    'item': 'Carried Interest',
                    'amount': -carry,
                    'category': 'Financing',
                },
                {
                    'item': 'Other Expenses',
                    'amount': -(expenses + other),
                    'category': 'Operating',
                },
                {
                    'item': 'Net Cash Flow',
                    'amount': net_cf,
                    'category': 'Total',
                },
            ]
            if ending_cash is not None:
                cf_consolidated.append({
                    'item': 'Cash & Equivalents (Latest)',
                    'amount': ending_cash,
                    'category': 'Balance',
                })
            data['cf_period'] = cf_period

